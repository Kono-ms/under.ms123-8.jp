function SendMail($mid, $midt)
{

	eval(globals());

	$maildata = GetMailTemplate('直接お問い合わせ(M1)');
	$MailBody = $maildata['BODY'];
	$subject = $maildata['TITLE'];

	// 相手の情報
	$StrSQL="SELECT * FROM DAT_M1 where MID='".$midt."'";
	$rs=mysqli_query(ConnDB(),$StrSQL);
	$item_midt=mysqli_fetch_assoc($rs);
	$email_midt = $item_midt['EMAIL'];
	$MailBody=str_replace("[MIDT_NAME]",$item_mid['M1_DVAL01'],$MailBody);
	
	// 自分の情報
	$StrSQL="SELECT * FROM DAT_M2 where MID='".$mid."'";
	$rs=mysqli_query(ConnDB(),$StrSQL);
	$item_mid=mysqli_fetch_assoc($rs);
	$email_mid = $item_mid['EMAIL'];
	$MailBody=str_replace("[MID_NAME]",$item_mid['M2_DVAL01'],$MailBody);

	$MailBody=str_replace("[COMMENT]",$FieldValue[3],$MailBody);

	mb_language("Japanese");
	mb_internal_encoding("UTF-8");

	// 相手に送る
	$MailBody1=str_replace("[D-NAME]",$item_midt["M1_DVAL01"],$MailBody);
	mb_send_mail($email_midt, $subject, $MailBody1, "From:".mb_encode_mimeheader(mb_convert_encoding(SENDER_NAME,"ISO-2022-JP","AUTO"))."<".SENDER_EMAIL.">"); 
	// 自分に送る
	$MailBody2=str_replace("[D-NAME]",$item_mid["M2_DVAL01"],$MailBody);
	mb_send_mail($email_mid, $subject, $MailBody2, "From:".mb_encode_mimeheader(mb_convert_encoding(SENDER_NAME,"ISO-2022-JP","AUTO"))."<".SENDER_EMAIL.">"); 
}

